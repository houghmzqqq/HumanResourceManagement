	public void infomationCheck() throws MsgException
	{
		if(fOrgaName == null || "".equals(fOrgaName) || "请选择".equals(fOrgaName))
			throw new MsgException("一级机构不能为空！");
		if(sOrgaName == null || "".equals(sOrgaName) || "请选择".equals(sOrgaName))
			throw new MsgException("二级机构不能为空！");
		if(tOrgaName == null || "".equals(tOrgaName) || "请选择".equals(tOrgaName))
			throw new MsgException("三级机构不能为空！");
		if(postTypeName == null || "".equals(postTypeName) || "请选择".equals(postTypeName))
			throw new MsgException("职位类型不能为空！");
		if(postName == null || "".equals(postName) || "请选择".equals(postName))
			throw new MsgException("职位不能为空！");
		if(staffName == null || "".equals(staffName))
			throw new MsgException("姓名不能为空！");
//		if(Email == null || "".equals(Email))
//			throw new MsgException("EMAIl不能为空！");
		if(cellPhoneNum == null || "".equals(cellPhoneNum))
			throw new MsgException("手机不能为空！");
		if(QQ == null || "".equals(QQ))
			throw new MsgException("QQ不能为空！");
		if(address  == null || "".equals(address ))
			throw new MsgException("地址不能为空！");
		if(zipCode == null || "".equals(zipCode))
			throw new MsgException("邮编不能为空！");
		if(IDCardNum == null || "".equals(IDCardNum))
			throw new MsgException("身份证号码不能为空！");
		if(IDCardNum.trim().length() != 18)
			throw new MsgException("身份证号码应为18位！");
		if(telephoneNum.toCharArray().length > 7)
			throw new MsgException("电话号码必须为7位！");
		if(cellPhoneNum.toCharArray().length > 11)
			throw new MsgException("电话号码必须为11位！");
		if(QQ.toCharArray().length > 11)
			throw new MsgException("QQ号码不能超过11位！");
		if(zipCode.toCharArray().length > 6)
			throw new MsgException("邮编不能超过6位！");
		if("未定义/0".equals(wageStander))
			throw new MsgException("请选择薪酬标准！");
		if(!birthday.matches("[0-9]{8}"))
			throw new MsgException("生日格式错误，正确格式为“YYYYMMDD！”");
		if(!builtTime.matches("[0-9]{8}"))
			throw new MsgException("建档时间格式错误，正确格式为“YYYYMMDD！”");
		if(age == null || "".equals(age))
			throw new MsgException("年龄不能为空！");
	}
