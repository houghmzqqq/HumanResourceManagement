package domain;

public class WageStanderBean 
{
	private String wageStanderId = null;
	private String wageStander = null;
	private String maker = null;
	private String registor = null;
	private String registerTime = null;
	private String wageStanderName = null;
	private String wageAmount = null;
	
	public String getWageStanderId() {
		return wageStanderId;
	}
	public void setWageStanderId(String wageStanderId) {
		this.wageStanderId = wageStanderId;
	}
	public String getWageStander() {
		return wageStander;
	}
	public void setWageStander(String wageStander) {
		this.wageStander = wageStander;
	}
	public String getMaker() {
		return maker;
	}
	public void setMaker(String maker) {
		this.maker = maker;
	}
	public String getRegistor() {
		return registor;
	}
	public void setRegistor(String registor) {
		this.registor = registor;
	}
	public String getRegisterTime() {
		return registerTime;
	}
	public void setRegisterTime(String registerTime) {
		this.registerTime = registerTime;
	}
	public String getWageStanderName() {
		return wageStanderName;
	}
	public void setWageStanderName(String wageStanderName) {
		this.wageStanderName = wageStanderName;
	}
	public String getWageAmount() {
		return wageAmount;
	}
	public void setWageAmount(String wageAmount) {
		this.wageAmount = wageAmount;
	}
}
